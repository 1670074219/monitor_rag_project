def compare_vectors(vec1, vec2, threshold=0.4):
    """
    计算两个向量的相似度，并判断是否为同一个人
    :param vec1: 特征向量 A (list 或 numpy array)
    :param vec2: 特征向量 B (list 或 numpy array)
    :param threshold: 判定阈值 (ReID通常在 0.3 ~ 0.5 之间)
    """
    # 1. 转换为 numpy 数组
    v1 = np.array(vec1)
    v2 = np.array(vec2)

    # 2. 计算余弦相似度 (Cosine Similarity)
    # 公式: (A . B) / (|A| * |B|)
    dot_product = np.dot(v1, v2)
    norm_v1 = np.linalg.norm(v1)
    norm_v2 = np.linalg.norm(v2)

    # 防止除以 0 的错误
    if norm_v1 == 0 or norm_v2 == 0:
        similarity = 0.0
    else:
        similarity = dot_product / (norm_v1 * norm_v2)

    # 3. 输出结果
    print("-" * 30)
    print(f"🔢 相似度得分: {similarity:.4f} ({(similarity*100):.2f}%)")
    
    if similarity > threshold:
        print(f"✅ 判定结果: 是同一个人 (阈值: {threshold})")
    else:
        print(f"❌ 判定结果: 不是同一个人 (阈值: {threshold})")
    print("-" * 30)

    return similarity

